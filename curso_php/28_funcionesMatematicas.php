<?php

//pow()--> elevar a un exponente
echo "pow(5,3)= ".pow(5,3)."<br/>";//5 elevado a 3 (5*5*5) = 125

//sqrt()--> raiz cuadrada
echo "sqrt(9)= ".sqrt(9)."<br/>";// raiz cuadrada de 9 es 3(3*3=9)
echo "sqrt(25)= ".sqrt(25)."<br/>";// raiz cuadrada de 25 es 5(5*5=25)

//rand()-->calcular un numero aleatoriamente desde:
// número min hasta un número máx
echo "rand(1,100)= ".rand(1, 100)."<br/>";

//pi() = 3.1415
echo "pi()= ".pi()."<br/>";

//floor()--> redondea hacia abajo
echo "floor(4.3)= ".floor(4.3)."<br/>";//4
echo "floor(-4.3)= ".floor(-4.3)."<br/>";//-5
echo "floor(9.99)= ".floor(9.99)."<br/>";//9

//ceil()--> redondea hacia arriba
echo "ceil(4.3)= ".ceil(4.3)."<br/>";//5
echo "ceil(-4.3)= ".ceil(-4.3)."<br/>";//-4
echo "ceil(9.99)= ".ceil(9.99)."<br/>";//10

//round()--> redondear un numero float/decimales
echo "round(3.4)= ".round(3.4)."<br/>";//3
echo "round(3.5)= ".round(3.5)."<br/>";//4
echo "round(1.995,2)= ".round(1.995,2)."<br/>";//2
echo "round(1.955,2)= ".round(1.955,2)."<br/>";//2
echo "round(5.045,2)= ".round(5.045,2)."<br/>";//5.05

