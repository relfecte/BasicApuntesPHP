<?php
    //var_sump() --> funcion para que nos muestre el tipo de variable
    var_dump(10); //int(10)
    echo "<br>";
    var_dump(TRUE); //bool(true)
    echo "<br>";
    var_dump(false); //bool(false)
    echo "<br>";
    var_dump(7.5); //float(7.5)
    echo "<br>";
    var_dump("Hola Mundo"); //string(10) "Hola Mundo"
    echo "<br>";
    var_dump('Hola Mundo'); //string(10) "Hola Mundo"
    echo "<br>";


    //datos tipo booleanas
    echo TRUE . "<br>"; //muestra un 1
    echo FALSE . "<br>"; // no muestra nada, se representa con 0
    
    //datos tipo enteras o integer
    echo 10 ."<br>";
    echo -20 . "<br>";

    //datos tipo decimanles o float/double
    echo 7.5 . "<br>";
    echo -7.5 . "<br>";

    //datos tipo string o de texto
    echo "Hola Mundo<br>";
    echo 'Hola Mundo';
?>